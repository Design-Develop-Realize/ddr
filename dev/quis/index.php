<h1>the intro</h1>
<p>As part of a challenge I'm doing <a href="http://designdeveloprealize.com" target="blank">with these guys here</a>, I've designed this website. For the most part, I believe this has gone well. The design here is more experimental than anything, really. I wanted to try something a little bit bigger and bolder. Why? Well, most times, <a href="http://inoveryourhead.net/the-simple-guide-to-growing-some-ing-balls/" target="blank">I don't think we're bold enough</a>. I mean, I could play it safe, but there's too much to do and try to stick to the safer, smaller type of layouts. Sometimes, a bolder, more creative statement needs to be made; as I said, "Some statements should be as large as you are."</p>
<p class="quote">Think deeply. Dream big. Go boldly.</p>
<p>I suppose that, when it's all said and done, there's very little we will end up wishing we didn't take a chance on, right?</p>
